    private void cari() {
        Object header[] = {"Username","Status","Nama","Jabatan","Alamat","no_telp"};
        tabmode = new DefaultTableModel(null, header);
        tabelKaryawan.setModel(tabmode);

        int baris = tabelKaryawan.getRowCount();
        for (int i = 0; i < baris; i++) {
            tabmode.removeRow(i);
        }

        String sql_select = "select * from datakaryawan where username like '%"+txtCari.getText()
                +"%' or nama like '%"+txtCari.getText()+"%'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql_select);
            while (rs.next()) {
                String username = rs.getString("username");
                String status = rs.getString("status");
                String nama = rs.getString("nama");
                String jabatan = rs.getString("jabatan");
                String alamat = rs.getString("alamat");
		String no_telp = rs.getString("no_telp");
                String[] kolom = {username,status,nama,jabatan,alamat,no_telp};
                tabmode.addRow(kolom);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
