    protected void datatable(){
        Object[] Baris ={"Username","Status","Nama","Jabatan","Alamat","No. Telepon"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelKaryawan.setModel(tabmode);
        String sql = "select * from datakaryawan";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String username = hasil.getString("username");
                String status= hasil.getString("status");
                String nama= hasil.getString("nama");
                String jabatan= hasil.getString("jabatan");
                String alamat = hasil.getString("alamat");
                String no_telp = hasil.getString("no_telp");
                String[] data={username,status,nama,jabatan,alamat,no_telp};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }
    
