    private void no_supplier(){
        try {
            java.sql.Statement stat = conn.createStatement();
            String sql = "select * from datasupplier order by kd_supp desc";
            ResultSet hasil = stat.executeQuery(sql);
            if(hasil.next()){
                String kd_supp = hasil.getString("kd_supp").substring(3);
                String AN = "" +(Integer.parseInt(kd_supp)+1);
                String Nol = "";
                if(AN.length()==1)
                {Nol="00";}
                else if(AN.length()==2)
                {Nol="0";}
                else if(AN.length()==3)
                {Nol="";}
                txtKdCustomer.setText("Customer"+Nol+AN);
            }else{
                txtKdCustomer.setText("Customer001");
                   }
            hasil.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    protected void datatable(){
        Object[] Baris={"Kode Customer","Nama Customer","No. Telepon","Alamat"};
        tabmode = new DefaultTableModel(null,Baris);
        tabelDataCustomer.setModel(tabmode);
        String sql = "select * from datacustomer";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String kd_cust = hasil.getString("kd_cust");
                String nm_cust = hasil.getString("nm_cust");
                String no_tlp = hasil.getString("no_tlp");
                String alamat = hasil.getString("alamat");
                String[] data = {kd_cust,nm_cust,no_tlp,alamat};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }
