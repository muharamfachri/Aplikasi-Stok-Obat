    private void cari() {
         Object header[] = {"Kode Customer","Nama Customer","No. Telepon","Alamat"};
        tabmode = new DefaultTableModel(null, header);
        tabelDataCustomer.setModel(tabmode);

        int baris = tabelDataCustomer.getRowCount();
        for (int i = 0; i < baris; i++) {
            tabmode.removeRow(i);
        }

        String sql_select = "select * from datacustomer where kd_cust like '%"+txtCari.getText()
                +"%' or nm_cust like '%"+txtCari.getText()+"%'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql_select);
            while (rs.next()) {
                String kd_cust = rs.getString("kd_cust");
                String nm_cust = rs.getString("nm_cust");
                String no_tlp = rs.getString("no_tlp");
		String alamat = rs.getString("alamat");
                String[] kolom = {kd_cust,nm_cust,no_tlp,alamat};
                tabmode.addRow(kolom);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
